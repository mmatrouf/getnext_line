#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	j;
	int		i;

	i = ft_strlen(s1);
	j = 0;
	while (s2[j] != '\0' && j < n)
	{
		s1[i] = s2[j];
		i++;
		j++;
	}
	s1[i] = '\0';
	return (s1);
}
