#include "libft.h"

int	ft_atoi(const char *str)
{
	unsigned int	r;
	int				s;
	int				i;

	s = 1;
	r = 0;
	i = 0;
	while ((str[i] <= 13 && str[i] >= 9) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			s = -1;
		i++;
	}
	while (ft_isdigit((int)str[i]))
	{
		r = r * 10 + str[i] - 48;
		i++;
	}
	return (r * s);
}
