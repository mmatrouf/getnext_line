#include "libft.h"

char	*ft_strcat(char *restrict s1, const char *restrict s2)
{
	int j;
	int i;

	i = ft_strlen(s1);
	j = 0;
	while (s2[j] != '\0')
	{
		s1[i] = s2[j];
		i++;
		j++;
	}
	s1[i] = '\0';
	return (s1);
}
