#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int i;

	i = n;
	if (n < 0)
	{
		i = n * -1;
		ft_putchar('-');
	}
	if (i > 9)
	{
		ft_putnbr(i / 10);
		ft_putnbr(i % 10);
	}
	else
		ft_putchar(i + 48);
}
